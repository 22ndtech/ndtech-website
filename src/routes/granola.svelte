    <h1>Homemade Granola</h1>
    <br>
    <p><i>Jeff’s Great Aunt Pat gave us her recipe and we adapted it to our liking.  We eat this granola as cereal, on top of yogurt or snack on big chunks.  It is delicious and addicting!</i></p>
    <br>
    <p>Preheat oven to 350°</p>
    <p>Combine the following ingredients in a roasting pan or other deep pan, at least 13x9 in size.</p>
    <p>6 C. Quaker 1 Minute Oats<br>
        3 C. King Arthur Baking Company Whole Wheat Flour<br>
        1 ¾ C C&H Pure Golden Brown Cane Sugar<br>
        ½ C. Kretschmer Original Toasted Wheat Germ<br>
        1 C. Bob’s Red Mill Shredded Coconut<br>
        1 C. Blue Diamond Whole Natural Almonds, rough chopped<br>
        ½ C. Spice Islands Sesame Seeds<br>
        1 C. Post Bran Flakes cereal, crushed<br>
        2 Tbsp. McCormick ground Cinnamon</p>
        <br>
        <p>Whisk together:  1 C. Water, 1 C. Chosen Foods 100% Pure Avocado Oil, 1 Tbsp. Spice Islands Pure Vanilla extract.</p>
        <p>Add wet ingredients to dry and toss until entire mixture is moist.<br>
        Do not pack it down, keep it light and fluffy.</p>
        <p>Bake 30 minutes at 350°.  Turn oven down to 250° and bake 20 minutes.  Stir mixture and cook 20 more minutes. </p>
        <p>Turn oven off and leave in oven to cool – preferably overnight.  Crumble to desired consistency.</p>
        <p>Store in an airtight container.</p>
        <br>
        <p><i>If you want bigger snack size chunks, increase brown sugar to 1 ¾ C., Water to 1 ½ C., Oil to , 1 ½ C. and Vanilla to 2-3 Tbsp.  When stirring during baking go slow and be easy to keep larger chunks.</i></p>
        


