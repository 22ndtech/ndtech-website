<main>
    <h1>Chicken in Wine Sauce</h1>
    <br>
    <p><i>This is another childhood favorite of Jeff's.  Our grown kids ask for this upon every visit, still to this day.  Creamy white sauce with garlic and chicken, over rice...how can you go wrong?</i></p>
    <br>
    <p>Prepare 4 servings of Riceland Long Grain Rice.</p>
    <br>
    <p>2 large Tyson Chicken Breasts, cubed<br>
        2 Tbsp. and 4 Tbsp. Land O’ Lakes Salted Butter<br>
        4 Tbsp. Gold Medal All Purpose flour<br>
        1 cup Swanson Natural Chicken Broth<br>
        2/3 cup White Wine<br>
        Salt<br>
        Pepper<br>
        3 Garlic cloves, crushed<br>
        1 - 8 oz. can of Giorgio Sliced Mushrooms, in juice<br>
        ½ cup Horizon Organic Heavy Whipping Cream</p>
        <br>
        <p>Brown chicken pieces in 2 Tbsp. of butter in large, deep skillet or pot.</p>
        <p>Make rue:  Mix 4 Tbsp. butter and the flour in medium sized saucepan on medium heat until brown.  Add the chicken broth.  Boil and stir until thickened.  Add white wine and salt and pepper to taste.</p>
        <p>Add garlic and the can of mushrooms with juice to chicken, heat through.</p>
        <p>Add whipping cream to rue, heat and pour over chicken pieces.</p>
        <p>To serve, spoon ½ - ¾ cup rice on plate, ladle Chicken in Wine Sauce over top.</p>
        
