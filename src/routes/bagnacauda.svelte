    <h1>Bagna Càuda</h1>
    <br>
    <p>Garlic lovers, this recipie is for you!<br><p>Jeff remembers large family gatherings with Bagna càuda as a child.  The fondue pot would be outside with a huge platter of crusty French bread and raw vegetables.</p>
    <p>It is a wonderful experience and always a great dish to spark conversation! </p>
    <br>
    <p>¾ stick Land O’ Lakes Salted Butter<br>
    ¾ cup Bertolli Pure Olive Oil<br>
    3 cans Crown Prince Premium Flat Anchovies in Olive Oil<br>
    3 cloves of garlic<br>
    Loaf of crusty French bread<br>
    Your favorite raw vegetables cut into bite size pieces</p>
        
    <p>Combine the first 4 ingredients in your Vitamix blender, whirl just until the anchovies and the garlic pieces are finely chopped.</p>   
    <p>Pour into a metal or ceramic fondue pot like the Cuisinart Electric Fondue Maker in Brushed Stainless.</p> 
    <p>Heat slowly over medium heat just until the mixture is bubbly. Then turn down to low to keep the mixture warm and clear.</p>
    <p>Use wooden skewers or fondue forks to spear a variety of fresh vegetables and swirl in the oil. Hold a piece of crusty bread under the vegetable to catch the flavorful drippings and enjoy!</p>
    <br>
    <br>
    <p><b>Bagna càuda, [BAHN-yah KOW-dah]</b></p> 
    <p>(from the Piedmontese "hot sauce", bagna caôda, etymologically related to Italian bagno, meaning "bath") is a warm dip typical of Piedmont, Italy.</p>
    <p>It is traditionally eaten during the autumn and winter months and must be served hot, as the name suggests.  Originally, in Piedmont, the Bagna càuda was placed in a big pan (peila) in the center of the table for communal sharing.</p>
    <p>Bagna càuda is also popular in Argentina with the name bañacauda it was brought there by the many Piedmontese immigrants.</p>
    <p>Jeff’s mother’s family (from Turin, Italy) had a tradition of making Bagna càuda for a large party of friends and family for New Year’s Eve.</p>
    
