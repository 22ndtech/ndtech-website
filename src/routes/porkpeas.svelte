<main>
   <h1>Pork Chops and Peas</h1>
   <br>
   <p><i>This recipe is a childhood favorite of Jeff's.  Our kids loved it and they now make it themsleves.</i></p> 
   <br>
   <p>4 Kirkland Signature Pork Loin Top Loin Chops, Boneless<br>
      ¾ C. Progresso Italian Style  Bread Crumbs<br>
      1 Vital Farms Alfresco Pasture-Raised Grade A Egg<br>
      3 Tbsp. Crisco All-Vegetable Shortening<br>
      Salt<br>
      Pepper<br>
      3 – 14 oz. cans Green Giant Sweet Peas, with juice</p>
      <br>
   <p>Preheat oven to 350°</p>
   <br>
   <p>Trim all fat off pork chops.</p>
   <p>Beat egg in container large enough to coat a pork chop.  Put Italian breadcrumbs in a separate, but similar size container.</p>
   <p>Melt Crisco in a large skillet.</p>
   <p>Dip pork chops in egg, then bread crumbs, put in hot skillet.  Salt and pepper to taste.</p>
   <p>Once all pork chops are browned, remove from skillet and place in a rectangular casserole dish.   Pour juice (only) from peas over pork chops.</p>
   <p>Bake at 350° for 1 hour.</p>
   <p>Add peas and bake an additional 15 minutes.</p>
   

