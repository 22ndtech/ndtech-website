<main>
    <h1>Cheesy Sunny Chicken Stew</h1> <h3><i>aka Yumminess with Manchego on top!</i></h3>
    <br>
    <p><i>This is a Jeff & Kim original recipe. As we learned how to use our Instant Pot, we found making this stew to be a quick, easy, and absolutely delicious meal. I mean, who doesn't love melted cheese?!?!</i></p>
    <br>
    <p>1 – 2 lbs. Kirkland Signature Chicken Breast, cut into bite size pieces<br>
        3 – 4 Tbsp. Land O Lakes Salted Butter or 4th & Heart Original Grass-Fed Ghee<br>
        1 Delallo Sun-Dried Tomato in Extra Virgin Olive Oil, cut into small pieces<br>
        1 - 8 oz. can of Giorgio Sliced Mushrooms<br>
        1 Tbsp. McCormick Dried Crushed Rosemary or fresh<br>
        1 Tbsp. McCormick Dried Oregano<br>
        1 Tbsp. McCormick Dried Basil<br>
        Salt & Pepper, to taste<br>
        ¾ cup White Wine<br>
        8 – 10 Cara Mia Artichoke Hearts, in water, cut to bite sized pieces<br>
        1 large baking potato – peeeled and cut into bite sized pieces<br>
        1 – 14 oz. can of Swanson Natural Chicken Broth<br>
        Kirkland Signature Spanish 6-month Aged Manchego Cheese – sliced to broil on top in oven</p>
        <br>
        <p>Set Instant Pot to Sauté, medium heat.</p>
        <p>Add butter or ghee to Instant Pot, let it heat for a minute or two.</p>
        <p>Add chicken breast pieces.</p>
        <p>Let the chicken set and brown in Instant Pot approx. 5 mins.  You want them to brown and crisp up! (lots of popping grease, so be careful!)</p>
        <p>Stir in sun-dried tomato, mushrooms, herbs, and salt & pepper.</p>
        <p>Add white wine to deglaze the bottom of the Instant Pot. Literally takes 10 seconds!</p>
        <p>Add artichoke hearts and potatoes.  Stir to coat in seasonings.</p>
        <p>Add chicken broth.</p>
        <p>Put Instant Pot lid on.  Change setting to pressure cook.  Set for 20 mins.  Start.</p>
        <br>
        <p>Set oven to Broil – High.</p>
        <p>When Instant Pot is done.  Carefully release the pressure – go slow.  I put a paper towel over the release valve area to keep hot liquid from spraying.</p>
        <p>Ladle stew from Instant Pot into oven proof bowls.</p>
        <p>Cover each bowl with manchego cheese slices.</p>
        <p>Transfer bowls to oven/broiler for 10-15 mins., or until golden brown.</p>
        <br>
        <p>The bowls and stew will be extremely hot!  Let them rest a few minutes before trying to eat.</p>
        <br>
        <p><i>*you can add carrots, celery or any other veggies or herbs to the stew, as you like – also use your favorite cheese to melt…it all is amazing!</i></p>

