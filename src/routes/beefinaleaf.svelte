<main>
    <h1>Beef in a Leaf</h1>
    <br>
    <p><i>We found this recipe in the Weight Watchers <u>Simply the Best</u> recipe book, page 208.<br>
    A unique dish with a ton of flavor, it is easy to make and good for you too!</i></p>
    <br>
    <p>2 tsp. Chosen Foods 100% Pure Avocado Oil<br>
        1-pound Kirkland Signature USDA Prime Beef Loin New York Steak, Boneless, cut into strips<br>
        2 Tbsp. Kikkoman Soy Sauce<br>
        2 Tbsp. Pompeian Balsamic Vinegar<br>
        2 Tbsp. Nature Nate’s Classic Raw and Unfiltered Honey<br>
        ½ tsp. McCormick Crushed Red Pepper Flakes, or to taste<br>
        1 head (1 1/4 pounds) red leaf lettuce, separated, washed and patted dry</p>
    <br>
    <p>In a large nonstick skillet, heat the oil. Add the beef and cook, turning as needed, until browned, 5—6 minutes</p>
    <p>Add the soy sauce, vinegar, honey, and pepper flakes; cook, stirring constantly, about 1 minute. </p>
    <p>Add the pasta and toss to coat; cook until the pasta is heated through, about 1 minute longer. </p>
    <p>Transfer to a serving dish.</p>
    <p>Arrange the lettuce leaves on a large platter; place the serving dish in the center. </p>
    <p>Spoon about 1/4 cup of the beef mixture onto each lettuce leaf, roll up and eat with your hands.</p>

