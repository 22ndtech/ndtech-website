<h1>About 22<sup>nd</sup> Tech</h1>

<h2>Contributing - Producing - Working</h2>
<p>22<sup>nd</sup> Century Tech, Inc. is building a community that offers an opportunity for like minded, DIY builders, producers and visionaries to collaborate, in an open, transparent environment.  It is built from the ground up to be a meritocracy where the benefit you get out of the community is based on what you put in.  It was founded on the belief that life is not a zero sum game.  It is a cooperative effort where we can create win scenarios and grow the pool of value so everyone gets more.</p>
